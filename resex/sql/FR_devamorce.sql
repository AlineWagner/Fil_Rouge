CREATE TABLE FR_Categories(
   cat_id INT NOT NULL AUTO_INCREMENT,
   cat_name VARCHAR(50),
   PRIMARY KEY (cat_id)
)  ENGINE=INNODB;

CREATE TABLE FR_SubCategories(
    subcat_id INT NOT NULL AUTO_INCREMENT,
    cat_id INT,
    subcat_name VARCHAR(50),
    PRIMARY KEY (subcat_id),
    FOREIGN KEY (cat_id) REFERENCES FR_Categories (cat_id)
) ENGINE=INNODB;

CREATE TABLE FR_Positions(
   pos_id INT NOT NULL AUTO_INCREMENT,
   pos_name VARCHAR(50),
   PRIMARY KEY (pos_id)
) ENGINE=INNODB;

CREATE TABLE FR_Employees(
   emp_id INT NOT NULL AUTO_INCREMENT,
   pos_id INT,
   emp_ref VARCHAR(15),
   emp_surname VARCHAR(50),
   emp_name VARCHAR(50),
   PRIMARY KEY (emp_id),
   FOREIGN KEY (pos_id) REFERENCES FR_Positions (pos_id)
) ENGINE=INNODB;

CREATE TABLE FR_Countries(
   cou_id VARCHAR(2) NOT NULL,
   cou_name VARCHAR(50),
   PRIMARY KEY (cou_id)
) ENGINE=INNODB;

CREATE TABLE FR_Delivery(
   deli_id INT NOT NULL AUTO_INCREMENT,
   deli_nbr INT,
   deli_ref VARCHAR(50),
   deli_desc VARCHAR(50),
   PRIMARY KEY (deli_id)
) ENGINE=INNODB;

CREATE TABLE FR_Suppliers(
   sup_id INT NOT NULL AUTO_INCREMENT,
   sup_name VARCHAR(50),
   sup_address VARCHAR(50),
   sup_city VARCHAR(50),
   sup_zip VARCHAR(10),
   sup_phone VARCHAR(50),
   sup_mail VARCHAR(50),
   cou_id VARCHAR(50),
   PRIMARY KEY (sup_id),
   FOREIGN KEY (cou_id) REFERENCES FR_Countries (cou_id)
) ENGINE=INNODB;

CREATE TABLE FR_Clients(
   cli_id INT NOT NULL AUTO_INCREMENT,
   cli_ref VARCHAR(10),
   cli_surname VARCHAR(50),
   cli_name VARCHAR(50),
   cli_billaddress VARCHAR(50),
   cli_billcity VARCHAR(50),
   cli_billzip VARCHAR(10),
   cli_deliaddress VARCHAR(50),
   cli_delicity VARCHAR(50),
   cli_delizip VARCHAR(10),
   cli_phone VARCHAR(20),
   cli_mail VARCHAR(50),
   cli_password VARCHAR(100),
   cli_type TINYINT,
   cli_coeff INT,
   cou_id VARCHAR(50),
   PRIMARY KEY (cli_id),
   FOREIGN KEY (cou_id) REFERENCES FR_Countries (cou_id)
) ENGINE=INNODB;

CREATE TABLE FR_Sales(
   sales_id INT NOT NULL AUTO_INCREMENT,
   sales_num INT,
   sales_state VARCHAR(50),
   sales_billadd VARCHAR(50),
   sales_billdeli VARCHAR(50),
   sales_date DATETIME,
   sales_paydate DATE,
   sales_reduc INT,
   emp_id INT,
   cli_id INT,
   PRIMARY KEY (sales_id),
   FOREIGN KEY (emp_id) REFERENCES FR_Employees (emp_id),
   FOREIGN KEY (cli_id) REFERENCES FR_Clients (cli_id)
) ENGINE=INNODB;

CREATE TABLE FR_Products(
   pro_id INT NOT NULL AUTO_INCREMENT,
   pro_name VARCHAR(50),
   pro_label VARCHAR(150),
   pro_desc VARCHAR(500),
   pro_price DECIMAL(7,2),
   pro_pic VARCHAR(50),
   subcat_id INT,
   sup_id INT,
   PRIMARY KEY (pro_id),
   FOREIGN KEY (subcat_id) REFERENCES FR_SubCategories (subcat_id),
   FOREIGN KEY (sup_id) REFERENCES FR_Suppliers (sup_id)
) ENGINE=INNODB;

CREATE TABLE FR_Order_Details(
   orde_id INT NOT NULL AUTO_INCREMENT,
   orde_quantity INT,
   sales_id INT,
   pro_id INT,
   PRIMARY KEY (orde_id),
   FOREIGN KEY (sales_id) REFERENCES FR_Sales (sales_id),
   FOREIGN KEY (pro_id) REFERENCES FR_Products (pro_id)
) ENGINE=INNODB;

CREATE TABLE FR_Delivered(
   deli_id INT NOT NULL AUTO_INCREMENT,
   orde_id INT,
   quantity INT,
   deliadd VARCHAR(50),
   delidate DATE,
   PRIMARY KEY (orde_id, deli_id),
   FOREIGN KEY (orde_id) REFERENCES FR_Order_Details (orde_id),
   FOREIGN KEY (deli_id) REFERENCES FR_Delivery (deli_id)
) ENGINE=INNODB;






